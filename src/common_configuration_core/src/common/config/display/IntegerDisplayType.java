package common.config.display;

/**
 * An enum describing how to display a int value.
 * 
 * @author benobiwan
 * 
 */
public enum IntegerDisplayType
{
	/**
	 * Value for the spinner.
	 */
	SPINNER,

	/**
	 * Value for the slider.
	 */
	SLIDER,

	/**
	 * Value for the text field.
	 */
	TEXTFIELD;
}
